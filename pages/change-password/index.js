import ChangePassword from "@/components/ChangePasswordComp/ChangePassword";
import { getCookie } from "@/services/utils/cookieExtract";
import Head from "next/head";

export default function ChangePasswordPage({ accessToken }) {
  return (
    <div className="page_wrapper">
      <Head>
        <title>Portfolio - Change Password</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <ChangePassword accessToken={accessToken} />
      </main>
    </div>
  );
}

export async function getServerSideProps(ctx) {
  const { cookie } = ctx.req.headers;
  let accessToken = null;
  try {
    const token = getCookie("portfolio_2_0", cookie);
    accessToken = token;
  } catch (err) {
    accessToken = null;
  }
  // setting private route
  if (accessToken) {
    return {
      redirect: {
        destination: "/dashboard",
        permanent: false,
      },
    };
  }
  return {
    props: {
      accessToken: accessToken,
    },
  };
}
